<section class="secao4-sobre">
    <h2 class="tituloSecao4-sobre">Diagnóstico</h2>
    <h4>O diagnóstico da COVID-19 pode ser realizado a partir de critérios como:</h4>
    <ol>
        <li> O <b>DIAGNÓSTICO CLÍNICO</b> é realizado pelo médico atendente, que deve avaliar a possibilidade da doença, principalmente, em pacientes com a associação dos seguintes sinais e sintomas:</li>
        <ul>
            <li>Febre, que pode estar presente no momento do exame clínico ou referida pelo paciente (sensação febril) de ocorrência recente.</li>
            <li>Sintomas do trato respiratório (por exemplo, tosse, dispneia, coriza, dor de garganta)</li>
            <li>Outros sintomas consistentes incluindo, mialgias, distúrbios gastrointestinais (diarreia/náuseas/vômitos), perda ou diminuição do olfato (anosmia) ou perda ou diminuição do paladar (ageusia).</li>
        </ul>
        <p>
            Em crianças, além dos itens anteriores, considera-se também a obstrução nasal, a desidratação e a  falta de apetite (inapetência), na ausência de outro diagnóstico específico. Em idosos, deve-se considerar também, critérios específicos de agravamento como: síncope (desmaio ou perda temporária de consciência), confusão mental, sonolência excessiva, irritabilidade e falta de apetite (inapetência). O diagnóstico clínico da doença, também deve ser considerado em pacientes com doença grave do trato respiratório inferior sem causa clara, como é o caso de pacientes que se apresentem em Síndrome Respiratória Aguda Grave (SRAG). Nesta síndrome o indivíduo apresenta-se em franca dispneia/desconforto respiratório/dificuldade para respirar com saturação de oxigênio (O2) menor do que 95% em ar ambiente ou coloração azulada dos lábios ou rosto (cianose) ou queixa de pressão persistente no tórax. Em crianças, a SRAG apresenta-se com os sinais e sintomas anteriores, devendo ser observados sinais característicos de esforço respiratório, tais como,  os batimentos de asa de nariz, tiragem intercostal, e, por fim, alteração na coloração das extremidades que ficam azuladas (cianose).
        </p>
        <li>O <b>DIAGNÓSTICO CLÍNICO-EPIDEMIOLÓGICO</b> é realizado pelo médico atendente no qual considera-se:</li>
        <ul>
            <li>
                <p>
                    casos de paciente com a associação dos sinais e sintomas supracitados ou SRAG <b>MAIS</b> histórico de contato próximo ou domiciliar, nos últimos 14 dias antes do aparecimento dos sintomas, com caso confirmado laboratorialmente para COVID-19 e para o qual não foi possível realizar a investigação laboratorial específica.
                </p>
            </li>
        </ul>
        <li><b>DIAGNÓSTICO CLÍNICO-IMAGEM:</b></li>
        <ul>
            <li>
                <p>
                    caso de sintomas respiratório mais febre ou SRAG ou óbito por SRAG que não foi possível confirmar ou descartar por critério laboratorial <b>E</b> que apresente alterações tomográficas.
                </p>
            </li>
        </ul>
        <li><b>DIAGNÓSTICO LABORATORIAL</b> - Caso o paciente apresente os sintomas respiratórios mais febre ou SRAG. O profissional de saúde poderá solicitar os seguintes exames laboratoriais:</li>
        <ul>
            <li>
                <p>
                    De biologia molecular, (<b>RT-PCR</b> em tempo real) que diagnostica tanto a COVID-19, a Influenza ou a presença de Vírus Sincicial Respiratório (VSR) normalmente <b>até o oitavo dia</b> de início de sintomas.
                </p>
            </li>
            <li>
                <p>
                    Imunológico, que detecta, ou não, a presença de anticorpos em amostras coletadas <b>a partir do oitavo dia</b> de início dos sintomas. Sendo eles:
                </p>
            </li>
            <li>
                <p>
                    Ensaio imunoenzimático (Enzyme-Linked Immunosorbent Assay - ELISA);
                </p>
            </li>
            <li>
                <p>
                    Imunocromatografia (<b>teste rápido</b>) para detecção de anticorpos;
                </p>
            </li>
            <li>
                <p>
                    Imunoensaio por Eletroquimioluminescência (ECLIA).
                </p>
            </li>
            <li>
                <p>
                    Pesquisa de antígenos: resultado reagente para SARS-CoV-2 pelo método de Imunocromatografia para detecção de antígeno.
                </p>
            </li>
        </ul>
        <li><b>DIAGNÓSTICO LABORATORIAL EM INDIVÍDUO ASSINTOMÁTICO</b> (pessoa sem sintomas) que realizou:</li>
        <ul>
            <li>
                <p>
                    Exame de Biologia Molecular com resultado DETECTÁVEL para SARS-CoV-2 realizado pelo método <b>RT-PCR</b> em tempo real.
                </p>
            </li>
            <li>
                <p>
                    Exame de Imunológico com resultado REAGENTE para IgM e/ou IgA realizado pelos seguintes métodos:  Ensaio imunoenzimático (ELISA) e Imunocromatografia (<b>teste rápido</b>) para detecção de anticorpos.
                </p>
            </li>
        </ul>
    </ol>
</section>
